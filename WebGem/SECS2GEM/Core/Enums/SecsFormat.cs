namespace SECS2GEM.Core.Enums
{
    /// <summary>
    /// SECS-II数据格式定义
    /// </summary>
    /// <remarks>
    /// 格式码结构（1字节）：
    /// - 高6位 (b7-b2): 数据类型
    /// - 低2位 (b1-b0): 长度字节数（在实际编码时设置）
    /// 
    /// 此枚举值仅包含高6位的类型定义，不包含长度字节数。
    /// </remarks>
    public enum SecsFormat : byte
    {
        /// <summary>
        /// 列表类型 (000000 00)
        /// 包含零个或多个子数据项
        /// </summary>
        List = 0x00,

        /// <summary>
        /// 二进制数据 (001000 00)
        /// 原始字节数组
        /// </summary>
        Binary = 0x20,

        /// <summary>
        /// 布尔类型 (001001 00)
        /// true/false值
        /// </summary>
        Boolean = 0x24,

        /// <summary>
        /// ASCII字符串 (010000 00)
        /// 7位ASCII字符
        /// </summary>
        ASCII = 0x40,

        /// <summary>
        /// JIS-8字符串 (010001 00)
        /// 日本工业标准8位字符
        /// </summary>
        JIS8 = 0x44,

        /// <summary>
        /// 2字节本地化字符串 (010010 00)
        /// Unicode字符
        /// </summary>
        Unicode = 0x48,

        /// <summary>
        /// 8字节有符号整数 (011000 00)
        /// 范围: -9,223,372,036,854,775,808 到 9,223,372,036,854,775,807
        /// </summary>
        I8 = 0x60,

        /// <summary>
        /// 1字节有符号整数 (011001 00)
        /// 范围: -128 到 127
        /// </summary>
        I1 = 0x64,

        /// <summary>
        /// 2字节有符号整数 (011010 00)
        /// 范围: -32,768 到 32,767
        /// </summary>
        I2 = 0x68,

        /// <summary>
        /// 4字节有符号整数 (011100 00)
        /// 范围: -2,147,483,648 到 2,147,483,647
        /// </summary>
        I4 = 0x70,

        /// <summary>
        /// 8字节浮点数 (100000 00)
        /// IEEE 754 双精度
        /// </summary>
        F8 = 0x80,

        /// <summary>
        /// 4字节浮点数 (100100 00)
        /// IEEE 754 单精度
        /// </summary>
        F4 = 0x90,

        /// <summary>
        /// 8字节无符号整数 (101000 00)
        /// 范围: 0 到 18,446,744,073,709,551,615
        /// </summary>
        U8 = 0xA0,

        /// <summary>
        /// 1字节无符号整数 (101001 00)
        /// 范围: 0 到 255
        /// </summary>
        U1 = 0xA4,

        /// <summary>
        /// 2字节无符号整数 (101010 00)
        /// 范围: 0 到 65,535
        /// </summary>
        U2 = 0xA8,

        /// <summary>
        /// 4字节无符号整数 (101100 00)
        /// 范围: 0 到 4,294,967,295
        /// </summary>
        U4 = 0xB0
    }
}
